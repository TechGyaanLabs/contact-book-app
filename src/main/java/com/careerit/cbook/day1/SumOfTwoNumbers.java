package com.careerit.cbook.day1;

public class SumOfTwoNumbers {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;
        int sub = num1 - num2;
        System.out.println("The sum of "+num1+" and "+num2+" is "+sum);
        System.out.println("The sub of "+num1+" and "+num2+" is "+sub);
    }
}
